        public static void DumpColor()
        {
            Utils.CreateSaveDir();

            StringBuilder buffer = new();
            buffer.AppendLine("id,颜色");

            // 墙
            int id = 0;
            foreach (var c in WallColors)
            {
                id++;
                buffer.AppendLine($"{id},{c}");
            }

            var path = Utils.CombinePath("[wm]墙的颜色.csv");
            File.WriteAllText(path, buffer.ToString());

            // 图格
            id = -1;
            buffer.Clear();
            buffer.AppendLine("id,style,颜色");
            foreach (var obj in TileColors)
            {
                id++;
                var id2 = -1;
                foreach (var c in obj)
                {
                    if (c == "000000")
                        continue;
                    id2++;
                    buffer.AppendLine($"{id},{id2},{c}");
                }
            }
            path = Utils.CombinePath("[wm]图格的颜色.csv");
            File.WriteAllText(path, buffer.ToString());
        }

